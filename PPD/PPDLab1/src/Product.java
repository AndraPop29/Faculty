public class Product {
    String name;
    float price;
    int quantity;
    int initialQuantity;

    public Product(String name, float price, int quantity, int initialQuantity) {
        this.name = name;
        this.price = price;
        this.quantity = quantity;
        this.initialQuantity = initialQuantity;
    }
}
